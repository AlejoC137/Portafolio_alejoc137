function compara(productos, ref) {
  let idsNoEncontrados = [];

  // Iterar sobre todos los productos
  productos.forEach(producto => {
    // Iterar sobre los ingredientes de cada producto
    producto.ingredientes.forEach(ingrediente => {
      // Verificar si el _id del ingrediente existe en la referencia (items)
      let existeEnRef = ref.some(item => item._id === ingrediente.id);
      
      // Si no existe en ref, agregarlo a la lista de no encontrados
      if (!existeEnRef) {
        idsNoEncontrados.push(ingrediente.id);
      }
    });
  });

  // Devolver los IDs no encontrados como una cadena de texto
  return idsNoEncontrados.join(", ");
}
